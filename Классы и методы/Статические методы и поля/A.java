package com.example;

public class A {
    public static int a = 1;
    public static int b;

    public static void printVars() {
        System.out.println(a);
        System.out.println(b);
    }

    public static void main(String[] args) {
        // Вариант 1: Вызов через имя класса
        A.printVars();

        // Вариант 2: Вызов через экземпляр класса
        A instance = new A();
        instance.printVars();

        // Вариант 3: Вызов внутри другого статического метода
        printVars();

        // Вариант 4: Вызов внутри нестатического метода
        instance.nonStaticMethod();
    }

    public void nonStaticMethod() {
        printVars();
    }
}

/*Вызов через имя класса:
Это стандартный способ вызова статического метода. Он ясно показывает, что метод принадлежит классу, а не экземпляру.

Вызов через экземпляр класса:
Хотя это возможно, это может ввести в заблуждение, так как создает впечатление, что метод принадлежит экземпляру, а не классу.

Вызов внутри другого статического метода:
Статические методы могут вызывать друг друга напрямую, так как они принадлежат классу.

Вызов внутри нестатического метода:
Нестатические методы могут вызывать статические методы напрямую, так как статические методы принадлежат классу и доступны всем экземплярам.*/