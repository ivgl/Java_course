public class A {
    {
        System.out.println("logic (1) id= " + this.id);
    }

    static {
        System.out.println("static logic");
    }

    private int id = 1;

    public A(int id) {
        this.id = id;
        System.out.println("ctor id= " + id);
    }

    {
        System.out.println("logic (2) id= " + id);
    }
}

public class Main {
    public static void main(String[] args) {
        new A(100);
    }
}

/*Статический блок:

Статический блок выполняется один раз при загрузке класса, до создания любых объектов этого класса.
Вывод: static logic

Инициализация полей:

Поле id инициализируется значением 1 при создании объекта.

Логические блоки:

Логические блоки выполняются последовательно в порядке их размещения в коде.
Первый логический блок выполняется до инициализации поля id, поэтому id будет иметь значение по умолчанию (0 для int).
Вывод: logic (1) id= 0

Инициализация поля id:

Поле id инициализируется значением 1.

Второй логический блок:

Второй логический блок выполняется после инициализации поля id.
Вывод: logic (2) id= 1

Конструктор:

Конструктор выполняется после выполнения всех логических блоков.
В конструкторе поле id устанавливается в значение 100.

Вывод: ctor id= 100

Полный вывод программы:

static logic
logic (1) id= 0
logic (2) id= 1
ctor id= 100*/